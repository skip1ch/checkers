export function generateReview(result) {
  const { winner, history = [], mode = 'ai', level = 'medium' } = result || {};
  const won = winner === 'W';
  const wMoves = history.filter(m => m.white);
  const bMoves = history.filter(m => !m.white);
  const pCaps = wMoves.reduce((s, m) => s + m.caps.length, 0);
  const aCaps = bMoves.reduce((s, m) => s + m.caps.length, 0);
  const moves = history.length;
  const total = pCaps + aCaps;
  const eff = total > 0 ? Math.round(pCaps / total * 100) : 50;
  const pMulti = wMoves.filter(m => m.caps.length >= 2).length;
  const aMulti = bMoves.filter(m => m.caps.length >= 2).length;
  const earlyW = wMoves.slice(0, 8);
  const edgeMoves = earlyW.filter(m => m.to && (m.to[1] === 0 || m.to[1] === 7)).length;
  const centerMoves = earlyW.filter(m => m.to && m.to[1] >= 2 && m.to[1] <= 5 && m.to[0] >= 2 && m.to[0] <= 5).length;
  const tips = [];

  if (!won && aCaps >= pCaps + 5) {
    tips.push({ title: 'Изолированные шашки — главная проблема', text: `Вы потеряли ${aCaps} шашек, взяв лишь ${pCaps}. Главная причина больших потерь — одиночные шашки без «соседа» по диагонали. Такая шашка не может защититься от двойного удара (вилки). Правило: держите шашки парами или цепями — тогда они прикрывают друг друга.` });
  } else if (!won && eff >= 50) {
    tips.push({ title: 'Дамки решили исход — не взятия', text: `Вы взяли ${pCaps} шашек против ${aCaps} — почти поровну, но всё равно проиграли. Значит, противник прорвался в дамки раньше. Дамка в русских шашках летает по всей диагонали — она стоит как 3 обычных шашки. Уже с 10-го хода следите, чтобы хотя бы одна ваша шашка двигалась к последней линии.` });
  } else if (!won && aCaps > pCaps) {
    tips.push({ title: 'Тактика: используйте обязательное взятие', text: `Соотношение взятий ${pCaps}:${aCaps} — противник бил чаще. В русских шашках взятие обязательно: это значит, что грамотно расставленная «приманка» вынуждает противника бить в невыгодную позицию. Ставьте шашку под удар, если за ней стоит ответный бой — это классическая «жертва темпа».` });
  } else if (won && eff >= 65) {
    tips.push({ title: 'Тактическое давление — ваш стиль', text: `Соотношение взятий ${pCaps}:${aCaps} (${eff}% эффективность). Такой отрыв создаётся контролем центральных диагоналей: шашки на полях d4, e5, c5, f4 атакуют сразу в двух направлениях и вынуждают противника защищаться, а не создавать угрозы.` });
  } else {
    tips.push({ title: 'Вилка — главный приём в шашках', text: `Вы взяли ${pCaps} шашек, потеряв ${aCaps}. Для роста — ищите «вилки»: ход, который угрожает сразу двум шашкам противника. В русских шашках вилка особенно смертоносна: взятие обязательно, противник не может уйти от удара и вынужден отдать одну из шашек.` });
  }

  if (pMulti === 0 && moves > 15) {
    tips.push({ title: 'Серии взятий — ключевой приём', text: `За всю партию у вас не было ни одного многоскока. «Мельница» — удар сразу 2–4 шашек за один ход — это самый мощный тактический приём в шашках. Ищите позиции, где ваша шашка стоит под углом к двум шашкам противника через пустые клетки: это потенциальный многоскок.` });
  } else if (pMulti > 0 && pMulti >= aMulti) {
    tips.push({ title: `Серии взятий: ${pMulti} ваших против ${aMulti} противника`, text: `Вы использовали многоскоки${pMulti > aMulti ? ' чаще противника' : ' на уровне противника'}. Каждый многоскок — результат заранее построенной ловушки. Следующий уровень: «мельница» — позиция, где одна шашка проходит туда-обратно и бьёт по 2 шашки за ход.` });
  } else {
    tips.push({ title: 'Многоскоки противника изменили баланс', text: `Противник сделал ${aMulti} серий взятий против ваших ${pMulti}. Серия за один ход меняет баланс материала сразу на 2–3 шашки. Научитесь «читать» доску на 2 хода вперёд: если между двумя вашими шашками есть свободная клетка, и за одной из них тоже свободно — противник может пройти насквозь.` });
  }

  if (edgeMoves >= 2) {
    tips.push({ title: 'Дебют: центр важнее краёв', text: `В первых ходах вы ${edgeMoves} раза выходили на крайние вертикали (a или h). Крайняя шашка имеет только одно направление атаки — она тактически «мертва». Золотое правило: первые 7–8 ходов занимайте центральные поля (c–f, ряды 3–6).` });
  } else if (centerMoves >= 3) {
    tips.push({ title: 'Центр занят — теперь давите флангами', text: `${centerMoves} из первых ходов — в центр: отличный дебют. Следующий шаг: когда центр захвачен, начинайте фланговое давление — продвигайте шашки по одному краю, чтобы создать угрозу прорыва в дамки.` });
  } else {
    tips.push({ title: 'Позиционная структура: держите «забор»', text: `Сильная позиция в шашках — это «забор»: цепь своих шашек по диагонали без разрывов. Такая структура не даёт противнику проникнуть в тыл и создаёт плацдарм для атаки. Никогда не двигайте шашки второй линии раньше времени.` });
  }

  if (!won && moves < 30) {
    tips.push({ title: 'Быстрое поражение: слабость второй линии', text: `Партия закончилась за ${moves} ходов. Это классическая дебютная ошибка: шашки второй линии были сдвинуты слишком рано, открыв дорогу к дамкам. Правило: вторую линию двигают последней. Сначала выстройте середину.` });
  } else if (!won && moves > 75) {
    tips.push({ title: 'Эндшпиль с дамками: оппозиция и углы', text: `${moves} ходов — длинная борьба. В эндшпиле с дамками работают два правила: 1) «Оппозиция» — не позволяйте вражеской дамке встать на ту же диагональ сзади вашей. 2) «Двойной угол» (a1–c1–a3): дамка в этом треугольнике почти неуязвима.` });
  } else if (mode === 'ai' && !won && level === 'hard') {
    tips.push({ title: 'Против сильного ИИ: позиционная игра', text: `«Сложный» бот просчитывает дерево на 5 ходов вперёд и не ошибается тактически. Против него работает только позиционная стратегия: медленно улучшайте позицию, избегайте острых разменов.` });
  } else if (mode === 'ai' && won && level === 'medium') {
    tips.push({ title: 'Средний уровень пройден — попробуйте сложный', text: `На «Сложном» бот не делает тактических ошибок и рассчитывает комбинации на 5 ходов. Там уже нельзя выиграть случайно — нужна системная игра.` });
  } else if (mode === 'friend') {
    tips.push({ title: 'Против живого игрока: используйте психологию', text: `В отличие от бота, живой игрок устаёт, торопится и делает интуитивные ошибки. Играйте на «темп»: делайте ходы быстро в очевидных позициях и долго думайте перед ключевыми.` });
  } else if (won) {
    tips.push({ title: 'Эндшпиль: ключ — количество дамок', text: `Вы победили — отлично. Для дальнейшего роста: в эндшпиле всё решает количество дамок. Разменивайте обычные шашки только если это ускоряет прорыв в дамки.` });
  } else {
    tips.push({ title: 'Техника: форсированное взятие как оружие', text: `Помните: в русских шашках взятие обязательно. Расставляйте позиции, где противник вынужден бить «в ловушку». Классический приём — «треугольник»: три ваших шашки создают позицию, где любое взятие ведёт к потере двух шашек противника.` });
  }

  return tips;
}

export function calcScore(result) {
  const { winner, history = [] } = result || {};
  const won = winner === 'W';
  const pCaps = history.filter(m => m.white).reduce((s, m) => s + m.caps.length, 0);
  const aCaps = history.filter(m => !m.white).reduce((s, m) => s + m.caps.length, 0);
  const moves = history.length;
  const total = pCaps + aCaps;
  const eff = total > 0 ? pCaps / total : 0.5;
  let score = won ? 55 : 20;
  score += Math.round(eff * 30);
  if (moves < 50) score += 8; else if (moves > 100) score -= 5;
  if (won && pCaps > aCaps * 1.5) score += 7;
  return Math.max(10, Math.min(100, score));
}
